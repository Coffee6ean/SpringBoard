# For Loops -
# Python for loops are like JS 'for ... of' loops:
#   -> for snack in ["Peanut", "Twizzler", "Mars Bar"]:
#          print("I ate a ", snack)    
colors = ["red", "orange", "yellow", "green"]
for color in colors:
    print(color)

print("------")
